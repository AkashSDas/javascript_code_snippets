//  ###### null and undefined ######

// =================================
// ### undefined ###

// Variables that are declared but not initialized to a value are undefined

// Examples:
var name;
var age;

name;  // undefined
age;   // undefined
// =================================
// ### null ###

// null is explicitly nothing

// Examples:
var currentPlayer = "charile";
currentPlayer = null; // game over

currentPlayer;  // null
